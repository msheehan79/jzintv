;;  WHEEL GRAPHICS                                                          ;;
;; ======================================================================== ;;
WHEELS      PROC

@@pnp       EQU     0 * 2  ;+1,  0, +1  for jumping
@@npn       EQU     1 * 2  ; 0, +1,  0  for jumping
@@nnn       EQU     2 * 2  ; 0,  0,  0  for jumping
@@mmm       EQU     3 * 2  ;-1, -1, -1  for jumping


            ;            @@
            ;        @@  @@  @@
            ;        @@  @@  @@
            ;   %%%%%@@ %%%%%@@ %%%%%
            ; %%%%%%%%%%%%%%%%%%%%%%%%
            DECLE  @@pnp

            ;             @@
            ;         @@  @@  @@
            ;         @@  @@  @@
            ;   %%%%% @@%%%%% @@%%%%%
            ; %%%%%%%%%%%%%%%%%%%%%%%%
            DECLE  @@pnp

            ;          @@  @@  @@
            ;          @@  @@  @@
            ;          @@  @@  @@
            ;   %%%%%   %%%%%   %%%%%
            ; %%%%%%%%%%%%%%%%%%%%%%%%
            DECLE  @@nnn

            ;           @@  @@  @@
            ;           @@  @@  @@
            ;           @@  @@  @@
            ;   %%%%%   %%%%%   %%%%%
            ; %%%%%%%%%%%%%%%%%%%%%%%%
            DECLE  @@nnn

            ;            @@      @@
            ;            @@  @@  @@
            ;            @@  @@  @@
            ;   %%%%%   %%%%%@@ %%%%%
            ; %%%%%%%%%%%%%%%%%%%%%%%%
            DECLE  @@npn

            ;             @@      @@
            ;             @@  @@  @@
            ;             @@  @@  @@
            ;   %%%%%   %%%%% @@%%%%%
            ; %%%%%%%%%%%%%%%%%%%%%%%%
            DECLE  @@npn

            ;              @@  @@  @@
            ;              @@  @@  @@
            ;              @@  @@  @@
            ;   %%%%%   %%%%%   %%%%%
            ; %%%%%%%%%%%%%%%%%%%%%%%%
            DECLE  @@nnn

            ;               @@  @@  @@
            ;               @@  @@  @@
            ;               @@  @@  @@
            ;   %%%%%   %%%%%   %%%%%
            ; %%%%%%%%%%%%%%%%%%%%%%%%
            DECLE  @@nnn


@@__m0      EQU     0000100000001000b
@@__m1      EQU     0000000000000000b
@@__n0      EQU     0000100000000000b
@@__n1      EQU     0000000000001000b
@@__p0      EQU     0000000000000000b
@@__p1      EQU     0000100000001000b
@@_m_0      EQU     0010000000100000b
@@_m_1      EQU     0000000000000000b
@@_n_0      EQU     0010000000000000b
@@_n_1      EQU     0000000000100000b
@@_p_0      EQU     0000000000000000b
@@_p_1      EQU     0010000000100000b
@@m__0      EQU     1000000010000000b
@@m__1      EQU     0000000000000000b
@@n__0      EQU     1000000000000000b
@@n__1      EQU     0000000010000000b
@@p__0      EQU     0000000000000000b
@@p__1      EQU     1000000010000000b

@@data:
            ;+1,  0, +1
            DECLE   @@p__0 + @@_n_0 + @@__p0
            DECLE   @@p__1 + @@_n_1 + @@__p1

            ; 0, +1,  0
            DECLE   @@n__0 + @@_p_0 + @@__n0
            DECLE   @@n__1 + @@_p_1 + @@__n1

            ; 0,  0,  0
            DECLE   @@n__0 + @@_n_0 + @@__n0
            DECLE   @@n__1 + @@_n_1 + @@__n1

            ;-1, -1, -1
@@jump      DECLE   @@m__0 + @@_m_0 + @@__m0
            DECLE   @@m__1 + @@_m_1 + @@__m1
            ENDP

